## 正文

- 一徑蒼寒長綠筠，雞聲人語隔林聞。
- 與誰共住祇明月，所可論交惟此君。
- 世事乘除天已定，人生勞逸念中分。
- 卜鄰倘許通來往，分我南山一片雲。

