## 正文

- 嚴精伐鼓趨晨雞，龍媒戒仗喑無嘶。
- 宰臣奏事左墄降，史臣曳履東階躋。
- 永康太守臣申之，上殿音吐干虹蜺。
- 曰臣前領大農簿，試郡得請今當西。
- 韃人求通反其使，金人匿壯招吾携。
- 勿言殘金繼前好，將貳韃靼摇青齊。
- 時將有反事有間，翫歲愒日憂噬臍。
- 皇帝瞿然曰是是，伸頤頷首天聽低。
- 貼黄更請磔叛將，褒恤死事不可稽。
- 陛下亦聞西事否，昔號險阻今多蹊。
- 散關皂郊已平地，白環黄牛猶潰隄。
- 分臺列閫越秦視，體統渙散人情睽。
- 上云私意誤大事，諄諄切切重提撕。
- 申之又言故臣丙，昔嘗有德于坤倪。
- 上顧指臣曰安丙，申之頓首如含悽。
- 紛紛交情異生死，獨此推本祛羣迷。
- 臣時張拱立帝傍，螭頭恨無千赫蹏。
- 爲王留行力不足，戴目處此羞梁鵜。
- 宮無小大莫非事，勇爲天子綏羣黎。
- 天如有意聽臣歸，問天重乞青玻瓈。

