## 正文

- 適親德範仰循循，遽喜名章肆筆成。
- 賸說臺高今勝昔，極知星聚暗還明。
- 當家翰藻爭春麗，上客詞源徹底清。
- 更共鄰翁閑指點，千峰環合水無聲。

