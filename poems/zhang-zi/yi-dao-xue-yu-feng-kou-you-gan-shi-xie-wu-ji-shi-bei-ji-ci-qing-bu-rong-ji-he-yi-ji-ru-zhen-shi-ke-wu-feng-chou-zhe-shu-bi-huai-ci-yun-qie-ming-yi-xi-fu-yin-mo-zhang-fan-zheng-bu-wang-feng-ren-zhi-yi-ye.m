## 正文

- 人間盛衰理甚明，勢去有如湯沃冰。
- 聽言一事足歎惋，恐君亦復傷中情。
- 行都赫奕名王第，列屋珠璣多秀慧。
- 主翁五十二本兵，寵冠諸家當盛世。
- 其間屬意字以端，姿品高妍兼妙藝。
- 燕開雲幄萃親友，艷陣香藂嘗竊睨。
- 轉眼陳迹俱已休，西風葉飛東水流。
- 相逢應怪我鬢改，貪情省認俄驚羞。
- 波明藏淚尚剪月，山淺带恨猶橫秋。
- 失身何心養兒女，啁啾耳厭山蠻語。
- 困居村店少人沽，飣餖腥鹹污盤俎。
- 無言脈脈話最深，爲君寫作溪婦吟。
- 人生榮悴由分定，有夫不必萌他心。

