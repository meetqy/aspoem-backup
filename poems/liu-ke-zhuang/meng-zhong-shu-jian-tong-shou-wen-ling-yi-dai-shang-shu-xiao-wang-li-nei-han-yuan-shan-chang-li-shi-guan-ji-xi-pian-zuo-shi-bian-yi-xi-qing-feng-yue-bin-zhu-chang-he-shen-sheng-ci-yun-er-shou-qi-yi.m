## 正文

- 懶即蓬萊訪具茨，平分不費一錢貲。
- 子綦隠几而聞者，太白停盃以問之。
- 螃蠏螯肥毋與事，琵琶調下莫題詩。
- 是知二老回頭笑，不料清源有此奇。

