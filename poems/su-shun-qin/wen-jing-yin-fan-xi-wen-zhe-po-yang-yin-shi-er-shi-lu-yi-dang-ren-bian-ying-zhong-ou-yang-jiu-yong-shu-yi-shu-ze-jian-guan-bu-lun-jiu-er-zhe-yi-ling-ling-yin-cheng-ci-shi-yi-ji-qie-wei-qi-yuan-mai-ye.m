## 正文

- 朝野蔚多士，衮然良可羞。
- 伊人秉直節，許國有深謀。
- 大議摇巖石，危言犯采旒。
- 蒼黄出京府，憔悴謫南州。
- 引黨俄嗟尹，移書遽竄歐。
- 安慙言得罪，要避曲如鉤。
- 郢路幾束馬，荆川還泝舟。
- 傷心眾山集，舉目大江流。
- 遠動家公念，深貽壽母憂。
- 橫身罹禍難，當路積仇讎。
- 衛上寧無術，亢宗非所優。
- 吾君思正士，莫賦畔牢愁。

