## 正文

- 神駒墮地無渥洼，象龍不復來流沙。
- 開元畫手老韓幹，爲作郭家師子花。
- 當年故物不堪看，踣鐡四蹄俱脱腕。
- 英姿逸態猶精神，仿彿風鬃血流汗。
- 龍眠畫足不畫蛇，意象異世同一家。
- 由來四大各假合，得此三昧無等差。
- 中郎名高自年少，畫工如山不同調。
- 人間意氣驚薾雲，筆下風流爾得妙。
- 王家人物小安豐，作詩論畫公所同。
- 一洗萬古凡馬空，九原誰喚杜陵翁。

