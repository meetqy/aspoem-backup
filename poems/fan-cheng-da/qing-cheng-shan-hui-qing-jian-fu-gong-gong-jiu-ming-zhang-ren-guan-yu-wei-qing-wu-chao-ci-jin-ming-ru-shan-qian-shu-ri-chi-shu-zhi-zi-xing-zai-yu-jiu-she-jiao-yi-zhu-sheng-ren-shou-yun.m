## 正文

- 墨詔東來洶驛傳，璇題金榜照山川。
- 祥開聖代千秋節，響動仙都九室天。
- 觸石湧雲埋紫邏，流金飛火燭蒼巔。
- 祗應老宅龐眉客，長記新宮錫號年。

