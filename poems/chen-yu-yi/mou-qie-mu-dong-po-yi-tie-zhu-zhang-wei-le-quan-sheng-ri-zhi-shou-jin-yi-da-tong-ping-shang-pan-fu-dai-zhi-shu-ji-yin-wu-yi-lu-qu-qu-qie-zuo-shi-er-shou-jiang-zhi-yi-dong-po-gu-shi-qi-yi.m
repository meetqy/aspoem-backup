## 正文

- 要學東坡壽樂全，此瓶端合供儒先。
- 鐵如意畔無憂畏，玉唾壺傍耐歲年。
- 項似董宣真是强，腹如邊孝故應便。
- 與公剩貯爲霖水，不羨宮門承露仙。

