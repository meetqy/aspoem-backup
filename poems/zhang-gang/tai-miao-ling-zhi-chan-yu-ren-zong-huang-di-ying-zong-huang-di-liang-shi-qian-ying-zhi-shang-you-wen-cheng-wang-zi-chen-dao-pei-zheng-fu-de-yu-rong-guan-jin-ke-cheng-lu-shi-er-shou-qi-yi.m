## 正文

- 吾皇聖德紹先猷，清廟華芝屢降休。
- 道盛百王神畫驗，祥開兩室化功侔。
- 犧尊影照彤雲聚，藻梲光連瑞氣浮。
- 願詔樂章歌偉蹟，更從來歲薦郊丘。

