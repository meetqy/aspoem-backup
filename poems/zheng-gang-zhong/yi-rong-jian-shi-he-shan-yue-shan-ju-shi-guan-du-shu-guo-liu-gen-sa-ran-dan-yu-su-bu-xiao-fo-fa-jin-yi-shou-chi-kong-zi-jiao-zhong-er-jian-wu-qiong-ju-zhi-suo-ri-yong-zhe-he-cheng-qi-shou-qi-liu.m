## 正文

- 瓜滿前疇菜滿畦，赤松屋北寺居西。
- 不村不郭常安穏，非律非禪自整齊。
- 靜見游魚潭底樂，任從幽鳥葉間啼。
- 此心得趣知誰解，一月寒光印碧溪。

