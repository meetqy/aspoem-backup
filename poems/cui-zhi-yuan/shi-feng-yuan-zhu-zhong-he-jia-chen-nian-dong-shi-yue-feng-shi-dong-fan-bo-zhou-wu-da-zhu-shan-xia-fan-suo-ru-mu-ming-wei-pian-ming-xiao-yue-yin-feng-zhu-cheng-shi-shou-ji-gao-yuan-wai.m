## 正文

- 巉嵒絕頂欲摩天，海日初開一朶蓮。
- 勢削不容凡樹木，格高唯惹好雲烟。
- 點蘇寒影粧新雪，戞玉清音噴細泉。
- 靜想蓬萊只如此，應當月夜會羣仙。

