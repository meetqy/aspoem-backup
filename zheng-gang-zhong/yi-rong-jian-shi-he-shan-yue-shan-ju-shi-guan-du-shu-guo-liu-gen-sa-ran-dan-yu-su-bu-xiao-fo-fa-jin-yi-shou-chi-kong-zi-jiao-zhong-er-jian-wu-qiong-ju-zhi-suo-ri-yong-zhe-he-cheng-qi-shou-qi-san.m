## 正文

- 陋屋三間草蓋成，四時蘭菊薦芳馨。
- 但知後圃多栽橘，何必陽山獨采苓。
- 破睡雪花茶滿盌，慰懷春色酒盈瓶。
- 與人無怨亦無德，自覺長年心自寧。

